<p>L&rsquo;association KARAMA est n&eacute;e en juillet 2012 dans le but d&rsquo;intervenir au <a href="https://fr.wikipedia.org/wiki/Mali" target="_blank">Mali</a>. Elle soutient&nbsp; des projets solidaires autour de l&rsquo;&eacute;ducation. L&rsquo;&eacute;cole n&rsquo;&eacute;tant pas obligatoire, de nombreux enfants ne sont pas scolaris&eacute;s.</p>

<p>Suite aux demandes des habitants Bozo (ethnie de p&ecirc;cheurs), un projet de construction d&rsquo;&eacute;cole a vu le jour.&nbsp;</p>

<p><strong><u>Notre approche, nos missions&nbsp;:</u></strong></p>

<p>R&eacute;orientation du projet en 2014&nbsp;:</p>

<p>Au d&eacute;part, il s&rsquo;agissait de construire une &eacute;cole destin&eacute;e aux enfants Bozo sur une &icirc;le au milieu du fleuve Niger &agrave; Bamako. Au cours de l&rsquo;ann&eacute;e 2013, nous nous sommes aper&ccedil;us que ce lieu pr&eacute;sentait de nombreuses difficult&eacute;s logistiques notamment d&rsquo;acc&egrave;s. Des dissensions au sein des familles r&eacute;sidant sur l&rsquo;&icirc;le pr&eacute;sageaient d&rsquo;autres probl&egrave;mes &agrave; venir notamment lors de la cr&eacute;ation d&rsquo;un comit&eacute; de gestion visant &agrave; organiser le bon d&eacute;roulement de l&rsquo;&eacute;cole.</p>

<p>C&rsquo;est pourquoi le projet s&rsquo;est r&eacute;orient&eacute; dans un village Bozo au bord du Niger dans le quartier de Badalabougou (en face de l&rsquo;&icirc;le). La plupart des enfants Bozo non scolaris&eacute;s habitent &agrave; cet endroit, il nous est donc paru &eacute;vident de d&eacute;velopper l&rsquo;&eacute;cole sur ce site.</p>

<p>Lors des r&eacute;unions avec le chef de village. Il a &eacute;t&eacute; soulev&eacute; une action plus urgente que la cr&eacute;ation de l&rsquo;&eacute;cole. Le village Bozo ne poss&egrave;de aucune arriv&eacute;e d&rsquo;eau potable. Il s&rsquo;agirait donc d&rsquo;installer dans un premier temps un robinet d&rsquo;eau communautaire g&eacute;r&eacute; par le chef de village. Il &eacute;tait certain pour nous de commencer les projets de l&rsquo;association par cette action qui am&eacute;liorera les conditions sanitaires de la population.</p>

<p><strong><u>Le village Bozo&nbsp;:</u></strong></p>

<p>Il&nbsp; fait parti des premiers lieux d&rsquo;habitation de Bamako et est aujourd&rsquo;hui compl&egrave;tement encastr&eacute; entre l&rsquo;ambassade d&rsquo;Espagne et des villas.&nbsp; Le village bozo est connu &agrave; Bamako pour la vente de poissons frais. Chaque jour, &agrave; l&rsquo;entr&eacute;e, une quinzaine de femmes accueillent les passants, les acheteurs, le sourire aux l&egrave;vres en pr&eacute;sentant toute sorte de poissons du fleuve. Les hommes se regroupent sous une grande paillote et r&eacute;parent les filets quand ils ne sont pas sur le fleuve. Certains p&ecirc;chent le jour &agrave; l&rsquo;&eacute;pervier, d&rsquo;autres la nuit.</p>

<p><strong><u>Une d&eacute;gradation sanitaire grandissante&nbsp;:</u></strong></p>

<p>Depuis quelques ann&eacute;es, la sortie des eaux us&eacute;es du quartier prend de plus en plus d&rsquo;ampleur. Cette&nbsp; rivi&egrave;re d&rsquo;eau noire&nbsp; traverse les maisons&nbsp; pour se d&eacute;verser&nbsp; &agrave; l&rsquo;endroit m&ecirc;me o&ugrave; les femmes lavent le linge, les enfants, la nourriture.</p>

